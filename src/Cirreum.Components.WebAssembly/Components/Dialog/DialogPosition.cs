namespace Cirreum.Components;

/// <summary>
/// The available fixed and predetermined locations for a dialog.
/// </summary>
public enum DialogPosition {
	/// <summary>
	/// The dialog is centered left to right and top to bottom.
	/// </summary>
	Center,
	/// <summary>
	/// The dialog is positioned in the center left area
	/// </summary>
	CenterLeft,
	/// <summary>
	/// The dialog is positioned in the centered right area
	/// </summary>
	CenterRight,
	/// <summary>
	/// The dialog is centered left to right and in the top area.
	/// </summary>
	Top,
	/// <summary>
	/// The dialog is positioned in the top left area
	/// </summary>
	TopLeft,
	/// <summary>
	/// The dialog is positioned in the top right area
	/// </summary>
	TopRight,
	/// <summary>
	/// The dialog is centered left to right and in the bottom area.
	/// </summary>
	Bottom,
	/// <summary>
	/// The dialog is positioned in the bottom left area
	/// </summary>
	BottomLeft,
	/// <summary>
	/// The dialog is positioned in the bottom right area
	/// </summary>
	BottomRight
}